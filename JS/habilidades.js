var habilidade1 = document.querySelector('#card__habilidade-1');

habilidade1.addEventListener("mouseover", function() {
    var nome = document.querySelector("#m1");
    var descricao = document.querySelector("#e1");
    
    nome.classList.remove("descricao");
    descricao.classList.remove("descricao");
});

habilidade1.addEventListener("mouseout", function() {
    var nome = document.querySelector("#m1");
    var descricao = document.querySelector("#e1");
    
    nome.classList.add("descricao");
    descricao.classList.add("descricao");
});
