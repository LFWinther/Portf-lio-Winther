var projeto1 = document.querySelector('#card__projeto-1');

projeto1.addEventListener("mouseover", function() {
    var nome = document.querySelector("#n1");
    var descricao = document.querySelector("#d1");
    
    nome.classList.remove("descricao");
    descricao.classList.remove("descricao");
});

projeto1.addEventListener("mouseout", function() {
    var nome = document.querySelector("#n1");
    var descricao = document.querySelector("#d1");
    
    nome.classList.add("descricao");
    descricao.classList.add("descricao");
});
